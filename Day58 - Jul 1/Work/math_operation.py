# math_operation.py

def add(a, b):
    """
    Add two numbers.

    :param a: First number
    :param b: Second number
    :return: Sum of the two numbers
    """
    return a + b

def subtract(a, b):
    """
    Subtract two numbers.

    :param a: First number
    :param b: Second number
    :return: Difference of the two numbers
    """
    return a - b
